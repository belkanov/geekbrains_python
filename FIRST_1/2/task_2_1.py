"""
Выяснить тип результата выражений:
15 * 3
15 / 3
15 // 2
15 ** 2

"""

# немного потестил =)
# в комментах указаны возможные типы при разных операндах

print(f'type(15 * 3) - {type(15 * 3)}')   # int/float
print(f'type(15 / 3) - {type(15 / 3)}')  # float
print(f'type(15 // 2) - {type(15 // 2)}')  # int/float
print(f'type(15 ** 2) - {type(15 ** 2)}')  # int/float
